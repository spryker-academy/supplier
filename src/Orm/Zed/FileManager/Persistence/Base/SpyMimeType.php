    /**
     * Performs the work of inserting or updating the row in the database.
     *
     * If the object is new, it inserts it; otherwise an update is performed.
     * All related objects are also updated in this method.
     *
     * @param ConnectionInterface $con
     * @return int The number of rows affected by this insert/update and any referring fk objects' save() operations.
     * @throws \Propel\Runtime\Exception\PropelException
     * @see save()
     */
    protected function doSave(ConnectionInterface $con): int
    {
        $affectedRows = 0; // initialize var to track total num of affected rows
        if (!$this->alreadyInSave) {
            $this->alreadyInSave = true;

            if ($this->isNew() || $this->isModified()) {
                // persist changes
                if ($this->isNew()) {
                    $this->doInsert($con);
                    $affectedRows += 1;
                } else {
                    $affectedRows += $this->doUpdate($con);
                }
                $this->resetModified();
            }

            $this->alreadyInSave = false;

        }

        return $affectedRows;
    }

    /**
     * Insert the row in the database.
     *
     * @param ConnectionInterface $con
     *
     * @throws \Propel\Runtime\Exception\PropelException
     * @see doSave()
     */
    protected function doInsert(ConnectionInterface $con): void
    {
        $modifiedColumns = [];
        $index = 0;

        $this->modifiedColumns[SpyMimeTypeTableMap::COL_ID_MIME_TYPE] = true;
        if (null !== $this->id_mime_type) {
            throw new PropelException('Cannot insert a value for auto-increment primary key (' . SpyMimeTypeTableMap::COL_ID_MIME_TYPE . ')');
        }

         // check the columns in natural order for more readable SQL queries
        if ($this->isColumnModified(SpyMimeTypeTableMap::COL_ID_MIME_TYPE)) {
            $modifiedColumns[':p' . $index++]  = 'id_mime_type';
        }
        if ($this->isColumnModified(SpyMimeTypeTableMap::COL_COMMENT)) {
            $modifiedColumns[':p' . $index++]  = 'comment';
        }
        if ($this->isColumnModified(SpyMimeTypeTableMap::COL_EXTENSIONS)) {
            $modifiedColumns[':p' . $index++]  = 'extensions';
        }
        if ($this->isColumnModified(SpyMimeTypeTableMap::COL_IS_ALLOWED)) {
            $modifiedColumns[':p' . $index++]  = 'is_allowed';
        }
        if ($this->isColumnModified(SpyMimeTypeTableMap::COL_NAME)) {
            $modifiedColumns[':p' . $index++]  = 'name';
        }

        $sql = sprintf(
            'INSERT INTO spy_mime_type (%s) VALUES (%s)',
            implode(', ', $modifiedColumns),
            implode(', ', array_keys($modifiedColumns))
        );

        try {
            $stmt = $con->prepare($sql);
            foreach ($modifiedColumns as $identifier => $columnName) {
                switch ($columnName) {
                    case 'id_mime_type':
                        $stmt->bindValue($identifier, $this->id_mime_type, PDO::PARAM_INT);

                        break;
                    case 'comment':
                        $stmt->bindValue($identifier, $this->comment, PDO::PARAM_STR);

                        break;
                    case 'extensions':
                        $stmt->bindValue($identifier, $this->extensions, PDO::PARAM_STR);

                        break;
                    case 'is_allowed':
                        $stmt->bindValue($identifier, (int) $this->is_allowed, PDO::PARAM_INT);

                        break;
                    case 'name':
                        $stmt->bindValue($identifier, $this->name, PDO::PARAM_STR);

                        break;
                }
            }
            $stmt->execute();
        } catch (Exception $e) {
            Propel::log($e->getMessage(), Propel::LOG_ERR);
            throw new PropelException(sprintf('Unable to execute INSERT statement [%s]', $sql), 0, $e);
        }

        try {
            $pk = $con->lastInsertId('spy_mime_type_pk_seq');
        } catch (Exception $e) {
            throw new PropelException('Unable to get autoincrement id.', 0, $e);
        }
        $this->setIdMimeType($pk);

        $this->setNew(false);
    }

